enum ContestComposition {

    /**
     * Les concours de pétanque peut se faire de manière différentes: 
     * - SELECTION : permet à l'équipe d'être formé lorsque de l'inscription
     * - SCRUM : permet de former aléatoirement les équipes lors du lancement
     * - ROTATING_SCRUM : permet de former aléatoirement les équipes lors de chaque tirage
     */

    SELECTION,
    SCRUM,
    ROTATING_SCRUM

}

export default ContestComposition;